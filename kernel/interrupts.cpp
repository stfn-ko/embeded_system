#include "interrupts.h"

///////////////////////////////////////////////////////////////////////////////
/// INTDisableMasterInterrupts
///
/// Disable global interrupts
///
/// @context: TASK
/// @scope: EXPORTED
/// @param: none
/// @return: none
///
//////////////////////////////////////////////////////////////////////////////

void INTDisableMasterInterrupts(void)
{
	asm("cli");
}

///////////////////////////////////////////////////////////////////////////////
/// INTEnableMasterInterrupts
///
/// Enable global interrupts
///
/// @context: TASK
/// @scope: EXPORTED
/// @param: none
/// @return: none
///
//////////////////////////////////////////////////////////////////////////////

void INTEnableMasterInterrupts(void)
{
	asm("sei");
}
